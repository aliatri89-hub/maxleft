const seen = new Set();
const uniqueFilms = films.filter(f => {
  if (seen.has(f.tmdb_id)) return false;
  seen.add(f.tmdb_id);
  return true;
});

async function checkCast(film) {
  const url = `https://api.themoviedb.org/3/movie/${film.tmdb_id}/credits?api_key=${TMDB_API_KEY}`;
  const res = await fetch(url);
  if (!res.ok) return null;
  const data = await res.json();
  const match = data.cast?.find(c => c.id === ACTOR_ID);
  return match ? { ...film, character: match.character, order: match.order } : null;
}

async function sleep(ms) { return new Promise(r => setTimeout(r, ms)); }

async function main() {
  console.log(`Checking ${uniqueFilms.length} films for ${ACTOR_NAME} (TMDB ID: ${ACTOR_ID})...\n`);
  const hits = [];

  for (let i = 0; i < uniqueFilms.length; i++) {
    const film = uniqueFilms[i];
    process.stdout.write(`[${i + 1}/${uniqueFilms.length}] [${film.feed.toUpperCase()}] ${film.title} (${film.year})... `);
    try {
      const result = await checkCast(film);
      if (result) {
        console.log(`✅ MATCH — "${result.character}" (billing #${result.order + 1})`);
        hits.push(result);
      } else {
        console.log('—');
      }
    } catch (e) {
      console.log(`ERROR: ${e.message}`);
    }
    if (i % 4 === 3) await sleep(1000);
  }

  const mainHits = hits.filter(h => h.feed === 'main');
  const patreonHits = hits.filter(h => h.feed === 'patreon');

  console.log('\n========== RESULTS ==========');
  console.log(`${ACTOR_NAME} appears in ${hits.length} BC films total (${mainHits.length} main feed, ${patreonHits.length} patreon-only)\n`);

  if (mainHits.length) {
    console.log('── MAIN FEED ──');
    mainHits.forEach(h => console.log(`  ${h.year} — ${h.title}\n         Character: "${h.character}" | Billing #${h.order + 1}`));
  }
  if (patreonHits.length) {
    console.log('\n── PATREON ONLY ──');
    patreonHits.forEach(h => console.log(`  ${h.year} — ${h.title}\n         Character: "${h.character}" | Billing #${h.order + 1}`));
  }

  console.log('\n========== TMDB IDs (main feed only) ==========');
  console.log(mainHits.map(h => h.tmdb_id).join(', '));
  console.log('\n========== TMDB IDs (patreon only) ==========');
  console.log(patreonHits.map(h => h.tmdb_id).join(', '));
  console.log('\n========== TMDB IDs (all) ==========');
  console.log(hits.map(h => h.tmdb_id).join(', '));
}

main();
