#!/usr/bin/env node
/**
 * scrape-bn-audio.js — Scrapes Books & Nachos Podbean pages
 * to extract mp3 URLs for all episodes.
 *
 * Run from Git Bash / terminal:
 *   node scrape-bn-audio.js
 *
 * Outputs: bn_episode_urls.sql (UPDATE statements for Supabase)
 *
 * How it works:
 * 1. Fetches paginated listing pages from booksandnachos.podbean.com
 * 2. Finds each episode's individual page URL
 * 3. Fetches each episode page to extract the audio enclosure URL
 * 4. Matches episode titles to community_items book titles
 * 5. Outputs SQL to set episode_url + extra_data.episode_url
 */

const NPP_ID = "dc0ff496-dce9-4dbf-b56e-5967be6b3b9c";
const BASE = "https://booksandnachos.podbean.com";
const DELAY_MS = 500; // Be polite to Podbean

const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

// ─── Normalize book title for matching ───
function norm(s) {
  return (s || "")
    .toLowerCase()
    .replace(/\s*\(.*?\)\s*/g, "")
    .replace(/\s*[-–—:]\s*part\s*\d+/gi, "")
    .replace(/\s*,\s*vol(?:ume)?\.?\s*\d+/gi, "")
    .replace(/[''""",.:;!?]/g, "")
    .replace(/^(the|a|an)\s+/i, "")
    .replace(/^(book review|now playing book review|bn book review)\s*/i, "")
    .replace(/\s+by\s+.*$/i, "")
    .replace(/\s+/g, " ")
    .trim();
}

async function fetchText(url) {
  const res = await fetch(url, {
    headers: { "User-Agent": "MANTL-BooksAndNachos-Scraper/1.0" },
  });
  if (!res.ok) throw new Error(`${res.status} ${url}`);
  return res.text();
}

async function main() {
  console.log("Scraping Books & Nachos episode pages...\n");

  // Step 1: Collect all episode page URLs from paginated listings
  const episodePages = [];
  for (let page = 1; page <= 20; page++) {
    const url = page === 1 ? BASE + "/" : `${BASE}/page/${page}/`;
    console.log(`  Fetching listing page ${page}...`);

    try {
      const html = await fetchText(url);

      // Extract episode links: href="/e/some-slug/"
      const epLinks = [...html.matchAll(/href="(https?:\/\/booksandnachos\.podbean\.com\/e\/[^"]+)"/g)]
        .map((m) => m[1])
        .filter((u) => !u.includes("#comments"));

      // Deduplicate (each episode appears twice — title + Read More)
      const unique = [...new Set(epLinks)];
      if (unique.length === 0) {
        console.log(`  → No episodes found, stopping pagination.`);
        break;
      }

      episodePages.push(...unique);
      console.log(`  → Found ${unique.length} episodes`);
      await sleep(DELAY_MS);
    } catch (e) {
      console.log(`  → Page ${page} failed: ${e.message}, stopping.`);
      break;
    }
  }

  console.log(`\nTotal episode pages found: ${episodePages.length}`);
  console.log("Fetching individual episode pages for audio URLs...\n");

  // Step 2: Fetch each episode page and extract audio URL
  const episodes = []; // { title, audioUrl }
  for (let i = 0; i < episodePages.length; i++) {
    const epUrl = episodePages[i];
    console.log(`  [${i + 1}/${episodePages.length}] ${epUrl.split("/e/")[1]?.slice(0, 40)}...`);

    try {
      const html = await fetchText(epUrl);

      // Look for <audio> src, or og:audio, or the podbean player data
      // Podbean pages typically have a meta tag or script with the mp3 URL
      let audioUrl = null;

      // Method 1: Look for enclosure-style audio URL in page source
      const mp3Match = html.match(/https:\/\/mcdn\.podbean\.com\/mf\/web\/[a-z0-9]+\/[^"'\s]+\.mp3/);
      if (mp3Match) {
        audioUrl = mp3Match[0];
      }

      // Method 2: Look for podbean player JSON
      if (!audioUrl) {
        const playerMatch = html.match(/"url"\s*:\s*"(https:\/\/mcdn\.podbean\.com[^"]+\.mp3)"/);
        if (playerMatch) audioUrl = playerMatch[1];
      }

      // Extract title from <title> tag
      const titleMatch = html.match(/<title>([^<]+)<\/title>/);
      const fullTitle = titleMatch
        ? titleMatch[1].replace(/\s*\|\s*Books\s*&\s*Nachos.*$/, "").trim()
        : epUrl.split("/e/")[1]?.replace(/-/g, " ") || "Unknown";

      if (audioUrl) {
        episodes.push({ title: fullTitle, audioUrl, pageUrl: epUrl });
      } else {
        console.log(`    ⚠ No audio URL found`);
        episodes.push({ title: fullTitle, audioUrl: null, pageUrl: epUrl });
      }
    } catch (e) {
      console.log(`    ✗ Error: ${e.message}`);
    }

    await sleep(DELAY_MS);
  }

  const withAudio = episodes.filter((e) => e.audioUrl);
  console.log(`\nExtracted audio for ${withAudio.length}/${episodes.length} episodes`);

  // Step 3: Generate SQL
  const lines = [
    "-- ============================================================",
    "-- NPP BOOKS: Episode audio URLs (full B&N catalog)",
    "-- Auto-generated by scrape-bn-audio.js",
    `-- Matched: ${withAudio.length} episodes`,
    "-- ============================================================",
    "",
  ];

  for (const ep of withAudio) {
    const titleEsc = ep.title.replace(/'/g, "''");
    const url = ep.audioUrl;
    lines.push(`-- ${ep.title}`);
    lines.push(`UPDATE community_items
SET episode_url = '${url}',
    extra_data = COALESCE(extra_data, '{}'::jsonb) || '{"episode_url": "${url}"}'::jsonb
WHERE title = '${titleEsc}' AND media_type = 'book'
  AND miniseries_id IN (SELECT id FROM community_miniseries WHERE community_id = '${NPP_ID}' AND tab_key = 'books');`);
    lines.push("");
  }

  // Also list unmatched for manual review
  const noAudio = episodes.filter((e) => !e.audioUrl);
  if (noAudio.length > 0) {
    lines.push("-- ═══ EPISODES WITHOUT AUDIO URL (manual review needed) ═══");
    for (const ep of noAudio) {
      lines.push(`-- ${ep.title} → ${ep.pageUrl}`);
    }
  }

  const fs = require("fs");
  const outFile = "bn_episode_urls_full.sql";
  fs.writeFileSync(outFile, lines.join("\n"));
  console.log(`\nSQL written to ${outFile}`);
  console.log("Run this in Supabase SQL Editor after verifying the output.");
}

main().catch(console.error);
